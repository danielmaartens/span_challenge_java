package com.danielmaartens;

public class MatchPoints {

    private String team;
    private Integer points;

    public void setTeam(String team) {
        this.team = team;
    }

    public void setPoints(Integer points) {
        this.points = points;
    }

    public String getTeam() {
        return this.team;
    }

    public Integer getPoints() {
        return this.points;
    }
}
