package com.danielmaartens;

public class TeamScore {

    private String team;
    private Integer score;

    public void setTeam(String team) {
        this.team = team;
    }

    public void setScore(Integer score) {
        this.score = score;
    }

    public String getTeam() {
        return this.team;
    }

    public Integer getScore() {
        return this.score;
    }
}
